public class Demo {
    public static void main(String[] args) {
        //八大基本数据类型

        //整数
        int num1 = 10;  //最常用
        byte num2 = 20;
        short num3 = 30;
        long num4 = 30L; //Long类型要在数字后面加L

        //小数:浮点数
        float  num5 = 50.1F;
        double num6 = 3.141592645789745132111845413;

        //字符
        char name = 'A'; //只可以存一个字符,用单引号
        String name2 = "fazz"; //String 是一个类，字符串要用双引号

        //布尔
        boolean name3 = false; // 布尔型只有false和true两个值

    }
}
